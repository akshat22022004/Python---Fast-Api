name = 'Harry'

nameshort = name[0:3] # start from zero all the way  till 3 excluding 3
print(nameshort) # Har


charecter1 = name[1] # second charecter
print(charecter1) # a


charecter2 = name[-1] # last charecter
print(charecter2) # y 

charecter3 = name[-2] # second last charecter
print(charecter3) # r


charecter4 = name[:2] # first two charecters
print(charecter4) # Ha


charecter5 = name[2:] # entire string from 2 entire length  same as 2 : 5
print(charecter5) # ry


